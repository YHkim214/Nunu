
public class DoWhileLoop2 {

	public static void main(String[] args) {
		//do-while 루프를 이용해 만든 3,6,9 게임
		for(int i = 1; i <= 100; i++) {
			System.out.printf("i = %d", i);
			
			int tmp = i;
			
			do {
				if(tmp % 10 % 3 == 0 && tmp % 10 != 0) {//tmp의 첫째 자릿수가 3의 배수인지 확인하는 라인. 0은 제외
					System.out.printf(" 짝!");
				}
			} while((tmp /= 10) != 0);
			System.out.println();
		}
	}

}
