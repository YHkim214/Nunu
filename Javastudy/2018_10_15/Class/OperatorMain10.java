
public class OperatorMain10 {

	public static void main(String[] args) {
		int a = 5, b = 7;
		
		a += b;
		System.out.println("a = " + a);//12
		
		a -= b;
		System.out.println("a = " + a);//5
		
		a *= b;
		System.out.println("a = " + a);//35
		
		a /= b;
		System.out.println("a = " + a);//5
		
		a %= b;
		System.out.println("a = " + a);//5
	}

}
