
public class OperatorMain01 {

	public static void main(String[] args) {
		int a = 6, b = 10;
		System.out.println(a + b);
		System.out.println(a - b);
		System.out.println(a * b);
		System.out.println((double)a / (double)b);
		System.out.println(a % b);
		
		System.out.println((20 + 5) * 3);
	}

}
